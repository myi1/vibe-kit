#!/usr/bin/env bash
# install.sh — set up vibe-kit on this machine.
# Writes wrappers (not symlinks) to ~/.local/bin/ so cross-machine clones don't
# break on path mismatch. Also registers skill/SKILL.md into ~/.claude/skills/vibe-retrofit/.
#
# Idempotent: safe to re-run after `git pull`.

set -eu

VIBE_KIT_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
BIN_DIR="${BIN_DIR:-$HOME/.local/bin}"
SKILL_DIR="${SKILL_DIR:-$HOME/.claude/skills/vibe-retrofit}"

_log()  { printf "%s\n" "$*" >&2; }
_ok()   { printf "\033[32m✓\033[0m %s\n" "$*" >&2; }
_warn() { printf "\033[33m!\033[0m %s\n" "$*" >&2; }
_die()  { printf "\033[31m✗\033[0m %s\n" "$*" >&2; exit 1; }

# ── Verify prerequisites ────────────────────────────────────────────────────
_log "Checking prerequisites…"
for cmd in git jq bash; do
  command -v "$cmd" >/dev/null 2>&1 || _die "Required command '$cmd' not found. Install it and retry."
done

# shasum or sha256sum
if ! command -v shasum >/dev/null 2>&1 && ! command -v sha256sum >/dev/null 2>&1; then
  _die "Need either 'shasum' (macOS) or 'sha256sum' (Linux). Neither found."
fi

_ok "Prerequisites OK."

# Optional but recommended
if ! command -v task-master >/dev/null 2>&1; then
  _warn "task-master CLI not installed. Tier 3 retrofits will fail."
  _warn "  Install with: npm i -g task-master-ai"
fi
if ! command -v bats >/dev/null 2>&1; then
  _warn "bats not installed. You can still use vibe-retrofit; tests just won't run locally."
  _warn "  Install with: brew install bats-core   (macOS)"
  _warn "             or: npm install -g bats     (any OS)"
fi

# ── Create BIN_DIR if missing ───────────────────────────────────────────────
if [ ! -d "$BIN_DIR" ]; then
  _log "Creating $BIN_DIR…"
  mkdir -p "$BIN_DIR" || _die "Failed to create $BIN_DIR"
fi

# ── Write wrapper for vibe-retrofit ─────────────────────────────────────────
WRAPPER="$BIN_DIR/vibe-retrofit"
_log "Writing wrapper: $WRAPPER"
cat > "$WRAPPER" <<EOF
#!/usr/bin/env bash
# Auto-generated by vibe-kit/bin/install.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ).
# Re-run install.sh after \`git pull\` if bin/ changed.
exec "$VIBE_KIT_ROOT/bin/vibe-retrofit" "\$@"
EOF
chmod +x "$WRAPPER"
_ok "Wrote wrapper at $WRAPPER"

# ── Register skill ──────────────────────────────────────────────────────────
if [ -f "$VIBE_KIT_ROOT/skill/SKILL.md" ]; then
  _log "Registering skill at ${SKILL_DIR}…"
  mkdir -p "$SKILL_DIR"
  cp "$VIBE_KIT_ROOT/skill/SKILL.md" "$SKILL_DIR/SKILL.md"
  _ok "Skill registered. Available as /vibe-retrofit in Claude Code."
else
  _warn "skill/SKILL.md not found. Skill not registered (only bash CLI available)."
fi

# ── Verify wrapper resolution ───────────────────────────────────────────────
case ":$PATH:" in
  *":$BIN_DIR:"*)
    _ok "$BIN_DIR is on \$PATH."
    ;;
  *)
    _warn "$BIN_DIR is NOT on \$PATH. Add this to your ~/.zshrc or ~/.bashrc:"
    _warn "  export PATH=\"\$HOME/.local/bin:\$PATH\""
    ;;
esac

# ── Summary ─────────────────────────────────────────────────────────────────
_log ""
_log "Install complete. Try:"
_log "  vibe-retrofit help"
_log "  vibe-retrofit version"
_log ""
_log "Next: cd into a project repo and run \`vibe-retrofit discover\`."
